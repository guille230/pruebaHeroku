                <h1>Tipos de cookies </h1>
            </div>
        </div>

        <div class="row">
            <div class="col">
                <p>Según quien sea la entidad que gestione el dominio desde donde se envían las cookies y trate los
                    datos que se obtengan se pueden distinguir dos tipos:</p>
                    <ul>
                        <li>
                            <span class="fw-bold">Cookies propias:</span> Aquellas que se envían al equipo terminal del usuario desde un equipo o
                            dominio gestionado por el propio editor y desde el que se presta el servicio solicitado por el
                            usuario.
                        </li>
                        <li>
                            <span class="fw-bold">Cookies de terceros:</span>  Aquellas que se envían al equipo terminal del usuario desde un equipo o
                            dominio que no es gestionado por el editor, sino por otra entidad que trata los datos obtenidos
                            través de las cookies.
                        </li>
                    </ul>
                <p>En el caso de que las cookies sean instaladas desde un equipo o dominio gestionado por el
                    propio editor pero la información que se recoja mediante éstas sea gestionada por un tercero, no
                    pueden ser consideradas como cookies propias.</p>
                <p>
                    Existe también una segunda clasificación según el plazo de tiempo que permanecen
                    almacenadas en el navegador del cliente, pudiendo tratarse de:
                </p>

                <ul>
                    <li>
                        <span class="fw-bold">Cookies de sesión:</span>  Diseñadas para recabar y almacenar datos mientras el usuario accede a
                        una página web. Suelen emplearse para almacenar información que solo interesa conservar para
                        la prestación del servicio solicitado por el usuario en una sola ocasión (p.e. una lista de productos
                        adquiridos)
                    </li>
                    <li>
                        <span class="fw-bold">Cookies persistentes:</span>  Los datos siguen almacenados en el terminal y pueden acceder y ser
                        tratados durante un periodo definido por el responsable de la cookie, y que puede ir de unos
                        minutos a varios años.
                    </li>
                </ul>
